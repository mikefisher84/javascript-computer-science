/**
 * SELECTION SORT
 *
 * Sorts the data by finding the smallest item and swapping it into the array
 * in the first unsorted location
 *
 * ALGORITHM:
 * 	Enumerate the array from the first unsorted item to the end
 * 	Identify the smallest item
 * 	Swap the smallest item with the first unsorted item
 *
 * PERFORMANCE
 * 	Worst case - O(n^2)
 * 		not appropriate for large unsorted data sets
 * 	Average case performance - O(n^2)
 * 		not appropriate for large unsorted data sets
 *		performs better than bubble sort but worse than insertion sort
 * 	Best case performance - O(n^2)
 * 		not appropriate for large unsorted data sets
 * 	Space Required - O(n)
 * 		operates directly on the input array meaning it is a candidate
 * 		algorithm when minimizing space is paramount
 */
